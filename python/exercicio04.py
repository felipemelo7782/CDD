numero1 = float(input("Insira um numero:"))
numero2 = float(input("Insira outro numero:"))

soma = numero1+numero2

subtracao = numero1-numero2

divisao = numero1/numero2

multiplicar = numero1*numero2

print("--------------------------------------")
print(f"divisao : {numero1} / {numero2} = {divisao:,.2f}")
print("--------------------------------------")
print(f"soma : {numero1} + {numero2} = {soma:,.2f}")
print("--------------------------------------")
print(f"subtracao : {numero1} - {numero2} = {subtracao:,.2f}")
print("--------------------------------------")
print(f"multiplicação : {numero1} x {numero2} = {multiplicar:,.2f}")
